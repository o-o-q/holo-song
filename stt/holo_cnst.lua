function Holo.song_lst_id(_s, cntry, cls)

  local lst_id = {}
  local excld_video_id = {}

  local cls_tbl = Holo["_"..cntry][cls]

  for name, tbl in pairs(cls_tbl) do

    ar.join(lst_id        , tbl.song.lst_id)

    if tbl.song.lst_excld_video_id then
      ar.join(excld_video_id, tbl.song.lst_excld_video_id)
    end
  end

  return lst_id, excld_video_id
end

function Holo.song_video(_s, cntry, cls)

  local video = {}

  for name, tbl in pairs(Holo["_"..cntry][cls]) do

    if tbl.song.video_id then

      for idx, video_id in pairs(tbl.song.video_id) do

        video[video_id] = {}
      end
    end
  end

  return video
end

